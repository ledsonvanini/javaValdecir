/**
 * Escreva um programa em Java com uma variável 
 * do tipo int que imprima na tela os números de 0 a 50.
 * Compile e poste o resultado aqui.
 */

public class LoopWhileZeroACinquenta {
    public static void main(String[] args) {
        int aux = 0;
        while(aux <= 50) {
            System.out.println("Nº: " + aux);
            aux++;
        }
    }
}
